public class Product {
    String name = "";
    double price = 0.0;
    int amount = 0;

    public Product(String name, double price, int amount) {
        this.name = name;
        this.price = price;
        this.amount = amount;
    }
}



//  Klasse:      Product
//  Attributen:   name, price, amount
//  Methodes:     constructor(name, price, amount)